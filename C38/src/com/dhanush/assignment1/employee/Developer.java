//Employee Package
package com.dhanush.assignment1.employee;

public class Developer {
	
	private String developerName;
	private int developerId;
	private int developerSalary;
	
	//Getter of the developerName
	public String getDeveloperName() {
		return developerName;
	
	}
	
	//Setter of the developerName
	public void setDeveloperName(String developerName) {
		this.developerName = developerName;
	
	}
	
	//Getter of the developerId
	public int getDeveloperId() {
		return developerId;
	
	}
	
	//Setter of the developerId
	public void setDeveloperId(int developerId) {
		this.developerId = developerId;
	
	}
	
	//Getter of the developerSalary
	public int getDeveloperSalary() {
		return developerSalary;
	
	}
	
	//Setter of the developerSalary
	public void setDeveloperSalary(int developerSalary) {
		this.developerSalary = developerSalary;
	}
	

}
